
public class Paxaros {
	
	String nombre;
	int patas;
	String tamanho;
	
	
	public Paxaros(String nombre, int patas, String tamanho) {
		super();
		this.nombre = nombre;
		this.patas = patas;
		this.tamanho = tamanho;
	}
	public String getNombre() {
		return nombre;
	}
	public void setNombre(String nombre) {
		this.nombre = nombre;
	}
	public int getPatas() {
		return patas;
	}
	public void setPatas(int patas) {
		this.patas = patas;
	}
	public String getTamanho() {
		return tamanho;
	}
	public void setTamanho(String tamanho) {
		this.tamanho = tamanho;
	}
	
	

}
